public class CW_operator {
    public static void main(String[] args){
        //arithmatic operator
        int a = 10;
        int b = 10 + a;
        System.out.println(b);
        //assignment operator
        int c = 9;
        c += 3;
        System.out.println(c);
        //comparison operator
        System.out.println(6==8);
        System.out.println(6==6);
        System.out.println(64>6);
        System.out.println(64<6);
        //logical operator
        System.out.println(64>6 && 64>67);
        System.out.println(64>6 || 64>67);
        //bitwise operator
        System.out.println(2&3);
    }
}
