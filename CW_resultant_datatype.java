public class CW_resultant_datatype {
    public static void main(String[] args){
       //resultant data type
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;
        System.out.println(b);

        //Increment and Decrement

        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int c = 55;
        System.out.println(c--);
        System.out.println(c);
        System.out.println(--c);
        System.out.println(c);
    }
}
